import random
import string
from locust import FastHttpUser, task, between, events

def random_string(length=10):
    """Generate a random string to ensure unique users and subreddits."""
    return ''.join(random.choices(string.ascii_letters + string.digits, k=length))

class RedditGatewayTester(FastHttpUser):
    # Wait between 1 and 2 seconds between tasks to simulate somewhat realistic user behavior
    wait_time = between(1.0, 2.0)

    def on_start(self):
        """
        on_start runs once per virtual user when they spin up.
        We use this to register, login, and create a personal subreddit.
        """
        self.username = f"user_{random_string()}"
        self.password = "password123"
        self.subreddit_name = f"sub_{random_string()}"

        # 1. Register
        self.client.post("/auth/register", data={
            "username": self.username,
            "password": self.password
        })

        # 2. Login (Locust automatically saves the 'access_token' cookie set by this response)
        self.client.post("/auth/login", data={
            "username": self.username,
            "password": self.password
        })

        # 3. Create a Subreddit for this user to post in
        self.client.post("/subreddit/create", data={
            "name": self.subreddit_name,
            "description": "A load testing subreddit"
        }, allow_redirects=False)

