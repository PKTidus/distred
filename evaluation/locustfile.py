import random
import string
from locust import FastHttpUser, task, between
from locust.exception import StopUser

# --- GLOBAL SHARED STATE ---
shared_post_ids = []
shared_subreddits = [] 

def random_string(length=10):
    return ''.join(random.choices(string.ascii_letters + string.digits, k=length))

def limit_actions(max_actions):
    """Custom decorator to kill the user after X actions."""
    def decorator(func):
        def wrapper(self, *args, **kwargs):
            self.action_count += 1
            if self.action_count > max_actions:
                raise StopUser()
            return func(self, *args, **kwargs)
        return wrapper
    return decorator

class RedditGatewayTester(FastHttpUser):
    wait_time = between(1.0, 2.0)

    def on_start(self):
        """Setup user, login, and initialize their baseline subreddit."""
        self.action_count = 0  
        
        self.username = f"user_{random_string()}"
        self.password = "password123"
        self.subreddit_name = f"sub_{random_string()}"

        # Register & Login
        self.client.post("/auth/register", data={"username": self.username, "password": self.password})
        self.client.post("/auth/login", data={"username": self.username, "password": self.password})

        # Create one initial Subreddit so the user has somewhere to post
        resp = self.client.post("/subreddit/create", data={
            "name": self.subreddit_name,
            "description": "Initial load testing subreddit"
        }, allow_redirects=False)
        
        if resp.status_code in (200, 301, 302):
            shared_subreddits.append(self.subreddit_name)


    # ==========================================
    # BROWSING TASKS: 69% Total
    # (Divided equally: 23 + 23 + 23 = 69)
    # ==========================================

    @task(23) 
    @limit_actions(1000)
    def view_home_feed(self):
        sort_by = random.choice(["hot", "top", "new"])
        self.client.get(f"/?sort={sort_by}", name="/ (Home Feed)")

    @task(23)
    @limit_actions(1000)
    def view_random_subreddit(self):
        if shared_subreddits:
            slug = random.choice(shared_subreddits)
            sort_by = random.choice(["hot", "top", "new"])
            self.client.get(f"/r/{slug}?sort={sort_by}", name="/r/[slug]")

    @task(23)
    @limit_actions(1000)
    def view_random_post(self):
        if shared_post_ids:
            post_id = random.choice(shared_post_ids)
            self.client.get(f"/post/{post_id}", name="/post/[id]")


    # ==========================================
    # VOTING TASKS: 29% Total
    # ==========================================

    @task(29)
    @limit_actions(1000)
    def vote_on_random_post(self):
        """Pure voting action, decoupled from post creation."""
        if shared_post_ids:
            random_post_id = random.choice(shared_post_ids)
            # Weighted random choice: 80% upvotes, 20% downvotes
            direction = "up" if random.random() > 0.2 else "down"
            
            self.client.post(f"/vote/{random_post_id}", data={
                "direction": direction
            }, allow_redirects=False, name="/vote/[id]")


    # ==========================================
    # CREATING TASKS: 2% Total
    # (Divided: 1% Posts, 1% Subreddits)
    # ==========================================

    @task(1) 
    @limit_actions(1000)
    def create_random_post(self):
        # Pick a random subreddit to post in (or fallback to their own)
        target_sub = random.choice(shared_subreddits) if shared_subreddits else self.subreddit_name
        
        post_response = self.client.post("/post/", data={
            "title": f"Load Test Post {random_string(5)}",
            "body": "This post was generated by Locust load testing.",
            "subreddit": target_sub
        }, allow_redirects=False, name="/post/ (Create)")

        if post_response.status_code in (301, 302):
            location_header = post_response.headers.get("Location")
            if location_header:
                post_id = location_header.rstrip('/').split('/')[-1]
                shared_post_ids.append(post_id)

    @task(1) 
    @limit_actions(1000)
    def create_random_subreddit(self):
        new_sub_name = f"sub_{random_string()}"
        resp = self.client.post("/subreddit/create", data={
            "name": new_sub_name,
            "description": "Dynamically created during test"
        }, allow_redirects=False, name="/subreddit/create")
        
        if resp.status_code in (200, 301, 302):
            shared_subreddits.append(new_sub_name)