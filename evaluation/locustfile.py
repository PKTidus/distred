import random
import string
from locust import FastHttpUser, task, between

# --- GLOBAL SHARED STATE ---
# Because Locust runs on a single thread utilizing gevent (asyncio), 
# appending to a standard Python list is safe and won't cause race conditions.
shared_post_ids = []

def random_string(length=10):
    return ''.join(random.choices(string.ascii_letters + string.digits, k=length))

class RedditGatewayTester(FastHttpUser):
    wait_time = between(1.0, 2.0)

    def on_start(self):
        self.username = f"user_{random_string()}"
        self.password = "password123"
        self.subreddit_name = f"sub_{random_string()}"

        # 1. Register
        self.client.post("/auth/register", data={
            "username": self.username,
            "password": self.password
        })

        # 2. Login
        self.client.post("/auth/login", data={
            "username": self.username,
            "password": self.password
        })

        # 3. Create a Subreddit
        self.client.post("/subreddit/create", data={
            "name": self.subreddit_name,
            "description": "A load testing subreddit"
        }, allow_redirects=False)

    @task
    def create_post_and_vote(self):
        # 4. Create a Post
        post_response = self.client.post("/post/", data={
            "title": f"Load Test Post {random_string(5)}",
            "body": "This post was generated by Locust load testing.",
            "subreddit": self.subreddit_name
        }, allow_redirects=False)

        # 5. Extract ID and share it with the swarm
        if post_response.status_code in (301, 302):
            location_header = post_response.headers.get("Location")
            if location_header:
                post_id = location_header.rstrip('/').split('/')[-1]
                # Add it to the global pool so OTHER users can vote on it
                shared_post_ids.append(post_id)

        # 6. Vote on a RANDOM post from the global pool (not necessarily your own)
        if shared_post_ids:
            random_post_id = random.choice(shared_post_ids)
            
            # Randomize up/down votes to simulate real traffic, weighted heavily to upvotes
            direction = "up" if random.random() > 0.1 else "down"
            
            self.client.post(f"/vote/{random_post_id}", data={
                "direction": direction
            }, allow_redirects=False)