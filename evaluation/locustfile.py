import random
import string
import time
from locust import FastHttpUser, LoadTestShape, task
from locust.exception import StopUser

# --- GLOBAL SHARED STATE ---
shared_post_ids = []
shared_subreddits = []


def random_string(length=10):
    return "".join(random.choices(string.ascii_letters + string.digits, k=length))


class StaircaseShape(LoadTestShape):
    stages = [
        {"duration": 60, "users": 10, "spawn_rate": 5},
        {"duration": 120, "users": 50, "spawn_rate": 10},
        {"duration": 180, "users": 100, "spawn_rate": 10},
        {"duration": 240, "users": 200, "spawn_rate": 20},
        {"duration": 300, "users": 500, "spawn_rate": 50},
    ]

    def tick(self):
        run_time = self.get_run_time()
        for stage in self.stages:
            if run_time < stage["duration"]:
                return stage["users"], stage["spawn_rate"]
        return None  # Returning None stops the test


class RedditGatewayTester(FastHttpUser):
    def post_with_retry(self, url, data, max_retries=5, backoff=2.0):
        for attempt in range(max_retries):
            with self.client.post(
                url, data=data, catch_response=True, allow_redirects=False
            ) as resp:
                if resp.status_code in (200, 201, 301, 302):
                    resp.success()
                    return resp
                elif resp.status_code in (500, 502, 503):
                    wait = backoff**attempt  # 1s, 2s, 4s, 8s, 16s
                    resp.failure(
                        f"{url} returned {resp.status_code}, retrying in {wait:.1f}s"
                    )
                    time.sleep(wait)
                else:
                    resp.failure(f"{url} unexpected {resp.status_code}")
                    return resp  # don't retry 4xx
        # Return a dummy-like indicator after exhausting retries
        raise StopUser()

    def on_start(self):
        self.action_count = 0
        self.authenticated = False
        self.username = f"user_{random_string()}"
        self.password = "password123"
        self.subreddit_name = f"sub_{random_string()}"

        self.post_with_retry(
            "/auth/register", {"username": self.username, "password": self.password}
        )
        self.post_with_retry(
            "/auth/login", {"username": self.username, "password": self.password}
        )

        self.authenticated = True

        self.post_with_retry(
            "/subreddit/create",
            {"name": self.subreddit_name, "description": "Load testing subreddit"},
        )
        shared_subreddits.append(self.subreddit_name)
        self._create_post(self.subreddit_name)

    def _create_post(self, target_sub):
        """Shared helper — creates a post and registers its ID globally."""
        resp = self.client.post(
            "/post/",
            data={
                "title": f"Load Test Post {random_string(5)}",
                "body": "This post was generated by Locust load testing.",
                "subreddit": target_sub,
            },
            allow_redirects=False,
            name="/post/ (Create)",
        )

        if resp and resp.status_code in (301, 302) and resp.headers:
            location = resp.headers.get("Location", "")
            post_id = location.rstrip("/").split("/")[-1]
            if post_id.isdigit():
                shared_post_ids.append(post_id)

    # ==========================================
    # BROWSING TASKS: 69% Total
    # ==========================================

    @task(23)
    def view_home_feed(self):
        sort_by = random.choice(["hot", "top", "new"])
        self.client.get(f"/?sort={sort_by}", name="/ (Home Feed)")

    @task(23)
    def view_random_subreddit(self):
        if shared_subreddits:
            slug = random.choice(shared_subreddits)
            sort_by = random.choice(["hot", "top", "new"])
            self.client.get(f"/r/{slug}?sort={sort_by}", name="/r/[slug]")

    @task(23)
    def view_random_post(self):
        if shared_post_ids:
            post_id = random.choice(shared_post_ids)
            self.client.get(f"/post/{post_id}", name="/post/[id]")

    # ==========================================
    # VOTING TASKS: 29% Total
    # ==========================================

    @task(29)
    def vote_on_random_post(self):
        if shared_post_ids:
            post_id = random.choice(shared_post_ids)
            direction = "up" if random.random() > 0.2 else "down"
            self.client.post(
                f"/vote/{post_id}",
                data={"direction": direction},
                allow_redirects=False,
                name="/vote/[id]",
            )

    # ==========================================
    # CREATING TASKS: 2% Total
    # ==========================================

    @task(1)
    def create_random_post(self):
        target_sub = (
            random.choice(shared_subreddits)
            if shared_subreddits
            else self.subreddit_name
        )
        self._create_post(target_sub)

    @task(1)
    def create_random_subreddit(self):
        new_sub_name = f"sub_{random_string()}"
        resp = self.client.post(
            "/subreddit/create",
            data={
                "name": new_sub_name,
                "description": "Dynamically created during test",
            },
            allow_redirects=False,
            name="/subreddit/create",
        )

        if resp.status_code in (200, 201, 301, 302):
            shared_subreddits.append(new_sub_name)
